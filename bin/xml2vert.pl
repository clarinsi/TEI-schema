#!/usr/bin/perl
# Finalise XSLT produced vertical file with UD annotations:
# - remove namespaces
# - de escape XML entities
# - add Slovene MSD
# - make lempos from lemma
use warnings;
use utf8;
binmode STDERR, 'utf8';
binmode STDIN, 'utf8';
binmode STDOUT, 'utf8';

while (<DATA>) {
    chomp;
    my ($msd_en, $msd_sl) = split;
    $msd_en2sl{$msd_en} = $msd_sl;
}
while (<>) {
    #Get rid of namespaces
    s|(<.+?) xmlns(:.*?)?=".*?"|$1|;
    s|&apos;|'|g;
    s|&amp;|&|g;
    if (/\t/) {
	s|&lt;|<|g;
	s|&gt;|>|g;
	s|&quot;|"|g;
	#print STDERR "$_";
	chomp;
	my ($word, $norm, $lemma, $msd_en,
	    $ud_pos, $ud_feats, $id, $ud_dep, 
	    $head_norm, $head_lemma, $head_msd_en,
	    $head_ud_pos, $head_ud_feats, $head_id) =
	    split /\t/;
	die "Bad MSD $msd_en in $_\n" unless exists $msd_en2sl{$msd_en};
	$msd_sl = $msd_en2sl{$msd_en};
	if ($head_msd_en eq '-') {$head_msd_sl = '-'}
	elsif (not exists $msd_en2sl{$head_msd_en}) {
	    die "Bad head MSD $head_msd_en in $_\n"
	}
	else {
	    $head_msd_sl = $msd_en2sl{$head_msd_en}
	}
	($msd_pos) = $msd_sl =~ /(.)/;
	$lempos = $lemma . '-' . lc($msd_pos);
	print join("\t", $word, $norm, $lempos, $msd_en, $msd_sl,
		   $ud_pos, $ud_feats, $id, $ud_dep, 
		   $head_norm, $head_lemma, $head_msd_en, $head_msd_sl,
		   $head_ud_pos, $head_ud_feats, $head_id) . "\n"
    }
    else {print}
}
